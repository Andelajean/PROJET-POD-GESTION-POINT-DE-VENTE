    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try{
         String req = "SELECT stock FROM vente WHERE article = '"+article.getText() +"' AND description = '"+description.getText()+"' ORDER BY id DESC";
    String ng  = nom.getText();
    String tel = telephone.getText();
    String art =article.getText();
    String desc = description.getText();
    String pu = prix_unitaire.getText();
    String pt = prix_total.getText();
    refference.setText(mat());
    String ref = refference.getText();
    int qte = (int) quantite.getValue();
    String va = String.valueOf(qte);
    if(ng.isEmpty()||tel.isEmpty()||art.isEmpty()||desc.isEmpty()||pu.isEmpty()||pt.isEmpty()||ref.isEmpty()){
        JOptionPane.showMessageDialog(null, "Veuillez remplir tous le champs");
    }
    else{
        connect();
         Statement st = con.createStatement();
        rs= st.executeQuery(req);
        pst=con.prepareStatement("INSERT INTO vente(nomclt,telct,numf,article,description,pu,qte,pt,nature_op,stock) VALUES (?,?,?,?,?,?,?,?,?,?)");
            pst.setString(1,ng);
            pst.setString(2,tel);
            pst.setString(3,ref);
             pst.setString(4,art);
            pst.setString(5,desc);
            pst.setString(6,pu);
             pst.setString(7,va);
            pst.setString(8,pt);
            pst.setString(9,"achat");
             if(rs.next()){
             int de = rs.getInt("stock");
            int stock = de+qte;
            String str = String.valueOf(stock); 
            pst.setString(10,str);
        }
             else{
                 pst.setString(10, va);
             }
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Ajout Effectué avec succès");
            con.close();
           
    }
        
    }catch(Exception e){
        e.printStackTrace();
    }
    
    
    }//GEN-LAST:event_jButton1ActionPerformed
 private String mat(){
      String code = "ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789";
     StringBuilder ma = new StringBuilder();
      Random sr = new Random();
     for(int i=0;i<=10;i++){
         ma.append(code.charAt(sr.nextInt(code.length())));
    
     }
     
     
     return ma.toString();
     
 }
 
